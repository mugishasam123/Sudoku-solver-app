import unittest
from scripts.modules.check_valid import valid

class TestTruthy(unittest.TestCase):

    def test_valid(self):
        m = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        
        self.assertTrue(valid(m, 0, 2, 4)) # Test valid value
        self.assertFalse(valid(m, 0, 2, 5)) # Test invalid value

if __name__ == '__main__':
    unittest.main()